numbers = [10, 20, 30, 40, 50, 60, 70, 80, 90, 30, 40]

count_30 = numbers.count(30)
count_40 = numbers.count(40)
count_50 = numbers.count(50)

print("Occurrence of 30:", count_30)
print("Occurrence of 40:", count_40)
print("Occurrence of 50:", count_50)


# output
# Occurrence of 30: 2
# Occurrence of 40: 2
# Occurrence of 50: 1